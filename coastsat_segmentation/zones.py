def coastsat_polygon(zone_id: str) -> list[list[float]]:
    """Return polygon as [[lng, lat], ...] for CoastSat (closed ring)."""
    coords = ZONES_LATLNG[zone_id]
    ring = [[lng, lat] for lat, lng in coords]
    if ring[0] != ring[-1]:
        ring.append(ring[0])
    return ring


def bbox_latlng(zone_id: str, pad_deg: float = 0.002) -> tuple[float, float, float, float]:
    """Return (lat_min, lng_min, lat_max, lng_max) padded by `pad_deg`."""
    coords = ZONES_LATLNG[zone_id]
    lats = [c[0] for c in coords]
    lngs = [c[1] for c in coords]
    return (min(lats) - pad_deg, min(lngs) - pad_deg,
            max(lats) + pad_deg, max(lngs) + pad_deg)
